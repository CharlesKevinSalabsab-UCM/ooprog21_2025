class Comments {
    public static void main(String args[]) {
        // this is a single line comment
        System.out.println("// indicates a single line comment");

        /* this is a a multiline comment
        as shown here
         */
        System.out.println("/**/ indicates a multiline comment");

        /**
         * this is a javadoc comment
         */
        System.out.println("/***/ indicates a java doc comment style");
    }
}